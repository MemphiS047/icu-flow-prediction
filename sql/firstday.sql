CREATE VIEW firsy_day AS 
SELECT * FROM blood_gas_first_day
JOIN blood_gas_first_day_arterial ON blood_gas_first_day.icustay_id=blood_gas_first_day_arterial.icustay_id
JOIN gcs_first_day ON blood_gas_first_day.icustay_id=gcs_first_day.icustay_id
JOIN height_first_day ON blood_gas_first_day.icustay_id=height_first_day.icustay_id
JOIN urine_output_first_day ON blood_gas_first_day.icustay_id=urine_output_first_day.icustay_id
JOIN ventilation_first_day ON blood_gas_first_day.icustay_id=ventilation_first_day.icustay_id
JOIN vitals_first_day ON blood_gas_first_day.icustay_id=vitals_first_day.icustay_id
JOIN weight_first_day ON blood_gas_first_day.icustay_id=weight_first_day.icustay_id
JOIN labs_first_day ON blood_gas_first_day.icustay_id=labs_first_day.icustay_id
JOIN rrt_first_day ON blood_gas_first_day.icustay_id=rrt_first_day.icustay_id
JOIN suspicion_of_infection ON blood_gas_first_day.icustay_id=suspicion_of_infection.icustay_id
JOIN everyscore ON blood_gas_first_day.icustay_id=everyscore.icustay_id